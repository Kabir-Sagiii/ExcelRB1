function findDay(num) {
  switch (num) {
    case 1:
      console.log("Today is Mon");

      break;
    case 2:
      console.log("Today is Tue");
      break;
    case 3:
      console.log("Today is Wed");
      break;

    default:
      console.log("IT is not matching with any case value");
  }
}

findDay(21);
